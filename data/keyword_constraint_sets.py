_SET_ID_TO_CATEGORY: Dict[str, str] = {
    sid: category
    for category, set_ids in KEYWORD_CONSTRAINT_CATEGORIES.items()
    for sid in set_ids
}

_SET_BY_ID: Dict[str, Dict[str, Any]] = {
    str(entry.get("set_id") or "").strip(): entry
    for entry in KEYWORD_CONSTRAINT_SETS
    if str(entry.get("set_id") or "").strip()
}


def _coerce_banned_list(items: Any) -> List[str]:
    if not isinstance(items, (list, tuple)):
        return []
    return [str(x).strip() for x in items if str(x).strip()]


def _coerce_target_expressions(items: Any) -> List[Dict[str, str]]:
    if not isinstance(items, (list, tuple)):
        return []
    out: List[Dict[str, str]] = []
    for raw in items:
        if isinstance(raw, dict):
            expr = str(raw.get("expr") or "").strip()
            if not expr:
                continue
            out.append({"expr": expr, "ko": str(raw.get("ko") or "").strip()})
        else:
            expr = str(raw or "").strip()
            if expr:
                out.append({"expr": expr, "ko": ""})
    return out


def _set_index(set_id: str) -> int:
    sid = str(set_id or "").strip()
    for index, entry in enumerate(KEYWORD_CONSTRAINT_SETS):
        if str(entry.get("set_id") or "").strip() == sid:
            return index
    return 0


def _patterns_for_combo(set_index: int, combo_id: str) -> List[str]:
    """Pick two patterns from the combo pool, sliding by set order (pairs cycle)."""
    pool = PATTERN_POOLS.get(str(combo_id or "").strip())
    if not pool or len(pool) < 2:
        return []
    start = (set_index * 2) % len(pool)
    return [pool[start], pool[(start + 1) % len(pool)]]


def get_keyword_constraint_category(set_id: str) -> str:
    sid = str(set_id or "").strip()
    return _SET_ID_TO_CATEGORY.get(sid, "기타")


def list_keyword_constraint_sets() -> List[Dict[str, str]]:
    """Catalog entries for topic-practice selection UI."""
    out: List[Dict[str, str]] = []
    for entry in KEYWORD_CONSTRAINT_SETS:
        if not isinstance(entry, dict):
            continue
        sid = str(entry.get("set_id") or "").strip()
        if not sid:
            continue
        out.append(
            {
                "set_id": sid,
                "title_ko": str(entry.get("title_ko") or sid).strip() or sid,
                "category": get_keyword_constraint_category(sid),
            }
        )
    return out


def list_keyword_constraint_sets_by_category() -> List[Dict[str, Any]]:
    """Group catalog entries by category (일상 → … → 기타)."""
    by_id = {entry["set_id"]: entry for entry in list_keyword_constraint_sets()}
    groups: List[Dict[str, Any]] = []
    for category in KEYWORD_CONSTRAINT_CATEGORY_ORDER:
        set_ids = KEYWORD_CONSTRAINT_CATEGORIES.get(category, ())
        sets = [by_id[sid] for sid in set_ids if sid in by_id]
        if sets:
            groups.append({"category": category, "sets": sets})
    return groups


def get_keyword_constraint_practice_set(set_id: str) -> List[Dict[str, Any]]:
    """Return bank-compatible rows (one per combo) for topic_practice_v2."""
    sid = str(set_id or "").strip()
    entry = _SET_BY_ID.get(sid)
    if not entry:
        return []

    combos = entry.get("combos")
    if not isinstance(combos, list) or not combos:
        return []

    rows: List[Dict[str, Any]] = []
    set_index = _set_index(sid)
    for combo in combos:
        if not isinstance(combo, dict):
            continue
        combo_id = str(combo.get("combo") or "").strip()
        combo_ko = str(combo.get("combo_ko") or combo_id).strip()
        question_text = str(combo.get("question_text") or "").strip()
        if not combo_id or not question_text:
            continue
        row_id = f"kc_{sid}_{combo_id}"
        opic_type = f"Keyword·{combo_ko}" if combo_ko else "Keyword"
        rows.append(
            {
                "id": row_id,
                "audio_id": row_id,
                "opic_type": opic_type,
                "topic_id": sid,
                "topic": sid,
                "combo": combo_id,
                "combo_ko": combo_ko,
                "question_text": question_text,
                "ko_helper": str(combo.get("ko_helper") or "").strip(),
                "question_kind": "keyword_constraint",
                "target_expressions": _coerce_target_expressions(combo.get("target_expressions")),
                "banned_expressions": _coerce_banned_list(combo.get("banned_expressions")),
                "patterns": _patterns_for_combo(set_index, combo_id),
            }
        )
    return rows
